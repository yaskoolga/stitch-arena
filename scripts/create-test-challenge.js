const { PrismaClient } = require('@prisma/client');

const prisma = new PrismaClient();

async function main() {
  const now = new Date();
  const oneWeekLater = new Date();
  oneWeekLater.setDate(now.getDate() + 7);

  const challenge = await prisma.challenge.create({
    data: {
      type: 'speed',
      title: 'Weekly Speed Challenge - Test',
      description: 'Stitch as many stitches as you can this week! This is a test challenge.',
      startDate: now,
      endDate: oneWeekLater,
      targetValue: 5000,
      isActive: true,
      isAutoGenerated: false,
    },
  });

  console.log('✅ Test challenge created:', challenge);
}

main()
  .catch((e) => {
    console.error('❌ Error:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
