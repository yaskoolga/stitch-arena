/**
 * Join a challenge (create participation record)
 */
export async function joinChallenge(
  userId: string,
  challengeId: string
): Promise<ChallengeParticipant> {
  const participant = await prisma.challengeParticipant.create({
    data: {
      userId,
      challengeId,
      currentProgress: 0,
    },
  });

  // Calculate initial progress
  const challenge = await prisma.challenge.findUnique({
    where: { id: challengeId },
  });

  if (challenge) {
    const progress = await calculateUserProgressForChallenge(userId, challenge);
    await updateLeaderboardEntry(challengeId, userId, progress);
  }

  return participant;
}

/**
 * Leave a challenge (delete participation record)
 */
export async function leaveChallenge(
  userId: string,
  challengeId: string
): Promise<void> {
  await prisma.challengeParticipant.delete({
    where: {
      challengeId_userId: {
        challengeId,
        userId,
      },
    },
  });

  // Also remove from leaderboard
  await prisma.challengeLeaderboard.deleteMany({
    where: {
      challengeId,
      userId,
    },
  });
}

/**
 * Update challenge progress for a user after creating a log
 * Called automatically when a new log is created
 */
export async function updateChallengeProgressForUser(
  userId: string,
  log: DailyLog
): Promise<void> {
  const now = new Date(log.date);

  // Find active challenges user is participating in
  const participations = await prisma.challengeParticipant.findMany({
    where: {
      userId,
      challenge: {
        isActive: true,
        startDate: { lte: now },
        endDate: { gte: now },
      },
    },
    include: {
      challenge: true,
    },
  });

  // Update progress for each challenge
  for (const participation of participations) {
    const progress = await calculateUserProgressForChallenge(
      userId,
      participation.challenge
    );
    await updateLeaderboardEntry(participation.challenge.id, userId, progress);
  }
}

/**
 * Get leaderboard for a challenge with pagination
 */
export async function getChallengeLeaderboard(
  challengeId: string,
  limit = 100,
  offset = 0
) {
  const entries = await prisma.challengeLeaderboard.findMany({
    where: { challengeId },
    include: {
      challenge: {
        select: {
          id: true,
          type: true,
          title: true,
        },
      },
    },
    orderBy: [{ rank: "asc" }, { score: "desc" }],
    take: limit,
    skip: offset,
  });

  // Get user info for each entry
  const enrichedEntries = await Promise.all(
    entries.map(async (entry) => {
      const user = await prisma.user.findUnique({
        where: { id: entry.userId },
        select: {
          id: true,
          name: true,
          avatar: true,
        },
      });

      return {
        ...entry,
        user,
      };
    })
  );

  const total = await prisma.challengeLeaderboard.count({
    where: { challengeId },
  });

  return {
    entries: enrichedEntries,
    total,
  };
}

/**
 * Check if a user is eligible to create challenges
 * Requirements: 30+ days since registration, 7+ day current streak
 */
export async function checkChallengeCreationEligibility(userId: string): Promise<{
  eligible: boolean;
  tenure: number;
  currentStreak: number;
  tenureRequired: number;
  streakRequired: number;
}> {
  const TENURE_REQUIRED = 30; // days
  const STREAK_REQUIRED = 7; // days

  // Get user info
  const user = await prisma.user.findUnique({
    where: { id: userId },
    select: { createdAt: true },
  });

  if (!user) {
    return {
      eligible: false,
      tenure: 0,
      currentStreak: 0,
      tenureRequired: TENURE_REQUIRED,
      streakRequired: STREAK_REQUIRED,
    };
  }

  // Calculate tenure (days since registration)
  const now = new Date();
  const tenure = Math.floor(
    (now.getTime() - user.createdAt.getTime()) / (1000 * 60 * 60 * 24)
  );

  // Get user's current streak
  const logs = await prisma.dailyLog.findMany({
    where: {
      project: { userId },
    },
    orderBy: { date: "desc" },
  });

  const { currentStreak } = calculateStreak(logs);

  // Check eligibility
  const eligible = tenure >= TENURE_REQUIRED && currentStreak >= STREAK_REQUIRED;

  return {
    eligible,
    tenure,
    currentStreak,
    tenureRequired: TENURE_REQUIRED,
    streakRequired: STREAK_REQUIRED,
  };
}

