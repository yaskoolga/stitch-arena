/**
 * Check if a user can edit a challenge
 * Requirements: user is creator, challenge hasn't started, no participants
 */
export async function canEditChallenge(
  challengeId: string,
  userId: string
): Promise<{ canEdit: boolean; reason?: string }> {
  const challenge = await prisma.challenge.findUnique({
    where: { id: challengeId },
    include: {
      _count: {
        select: { participants: true },
      },
    },
  });

  if (!challenge) {
    return { canEdit: false, reason: "Challenge not found" };
  }

  // Must be the creator
  if (challenge.createdBy !== userId) {
    return { canEdit: false, reason: "You are not the creator of this challenge" };
  }

  // Cannot edit auto-generated challenges
  if (challenge.isAutoGenerated) {
    return { canEdit: false, reason: "Cannot edit auto-generated challenges" };
  }

  // Cannot edit if already started
  const now = new Date();
  if (challenge.startDate <= now) {
    return { canEdit: false, reason: "Cannot edit a challenge that has already started" };
  }

  // Cannot edit if has participants
  if (challenge._count.participants > 0) {
    return { canEdit: false, reason: "Cannot edit a challenge with participants" };
  }

  return { canEdit: true };
}

/**
 * Check if a user can delete a challenge
 * Requirements: user is creator, (not started) OR (active with no participants)
 */
export async function canDeleteChallenge(
  challengeId: string,
  userId: string
): Promise<{ canDelete: boolean; reason?: string }> {
  const challenge = await prisma.challenge.findUnique({
    where: { id: challengeId },
    include: {
      _count: {
        select: { participants: true },
      },
    },
  });

  if (!challenge) {
    return { canDelete: false, reason: "Challenge not found" };
  }

  // Must be the creator
  if (challenge.createdBy !== userId) {
    return { canDelete: false, reason: "You are not the creator of this challenge" };
  }

  // Cannot delete auto-generated challenges
  if (challenge.isAutoGenerated) {
    return { canDelete: false, reason: "Cannot delete auto-generated challenges" };
  }

  const now = new Date();

  // Can delete if not started yet
  if (challenge.startDate > now) {
    return { canDelete: true };
  }

  // Can delete if active but no participants
  if (challenge.endDate >= now && challenge._count.participants === 0) {
    return { canDelete: true };
  }

  // Cannot delete past or active challenges with participants
  return {
    canDelete: false,
    reason: "Cannot delete a challenge with participants or that has ended",
  };
}
