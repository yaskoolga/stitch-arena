/**
 * Challenge system logic
 */

import { prisma } from "./prisma";
import { Challenge, ChallengeParticipant, DailyLog } from "@prisma/client";
import { startOfWeek, endOfWeek, addDays, differenceInDays } from "date-fns";
import { calculateStreak } from "./stats";

/**
 * Calculate user's progress for a challenge based on type
 */
export async function calculateUserProgressForChallenge(
  userId: string,
  challenge: Challenge
): Promise<number> {
  const startDate = new Date(challenge.startDate);
  const endDate = new Date(challenge.endDate);

  // Get all user's logs within challenge period
  const logs = await prisma.dailyLog.findMany({
    where: {
      project: {
        userId,
      },
      date: {
        gte: startDate,
        lte: endDate,
      },
    },
    orderBy: {
      date: "asc",
    },
  });

  // Calculate progress based on challenge type
  switch (challenge.type) {
    case "speed":
      // Sum of all daily stitches in period
      return logs.reduce((sum, log) => sum + log.dailyStitches, 0);

    case "streak":
      // Use existing streak calculation
      const { currentStreak } = calculateStreak(logs);
      return currentStreak;

    case "completion":
      // Number of completed projects during period
      const completedProjects = await prisma.project.count({
        where: {
          userId,
          status: "completed",
          updatedAt: {
            gte: startDate,
            lte: endDate,
          },
        },
      });
      return completedProjects;

    default:
      return 0;
  }
}

/**
 * Update or create leaderboard entry for a user in a challenge
 */
export async function updateLeaderboardEntry(
  challengeId: string,
  userId: string,
  score: number
): Promise<void> {
  await prisma.challengeLeaderboard.upsert({
    where: {
      challengeId_userId: {
        challengeId,
        userId,
      },
    },
    update: {
      score,
      updatedAt: new Date(),
    },
    create: {
      challengeId,
      userId,
      score,
      rank: 0, // Will be recalculated
    },
  });

  // Update participant progress as well
  await prisma.challengeParticipant.update({
    where: {
      challengeId_userId: {
        challengeId,
        userId,
      },
    },
    data: {
      currentProgress: score,
      lastUpdated: new Date(),
    },
  });
}

/**
 * Recalculate ranks for all participants in a challenge
 * Should be called periodically (cron job) or after significant updates
 */
export async function recalculateRanks(challengeId: string): Promise<void> {
  // Get all leaderboard entries sorted by score
  const entries = await prisma.challengeLeaderboard.findMany({
    where: { challengeId },
    orderBy: { score: "desc" },
  });

  // Assign ranks (handling ties)
  let currentRank = 1;
  let previousScore: number | null = null;
  let sameRankCount = 0;

  for (const entry of entries) {
    if (previousScore !== null && entry.score < previousScore) {
      currentRank += sameRankCount;
      sameRankCount = 1;
    } else {
      sameRankCount++;
    }

    await prisma.challengeLeaderboard.update({
      where: { id: entry.id },
      data: { rank: currentRank },
    });

    previousScore = entry.score;
  }
}

/**
 * Generate a weekly speed challenge
 * To be called by cron job every Monday
 */
export async function generateWeeklySpeedChallenge(): Promise<Challenge> {
  const now = new Date();
  const startDate = startOfWeek(now, { weekStartsOn: 1 }); // Monday
  const endDate = endOfWeek(now, { weekStartsOn: 1 }); // Sunday

  // Calculate week number
  const weekNumber = Math.ceil(
    (differenceInDays(startDate, new Date(startDate.getFullYear(), 0, 1)) + 1) / 7
  );

  const challenge = await prisma.challenge.create({
    data: {
      type: "speed",
      title: `Weekly Speed Challenge - Week ${weekNumber}`,
      description: `Stitch as many stitches as you can this week! Top 10 fastest stitchers will be featured on the leaderboard.`,
      startDate,
      endDate,
      targetValue: 10000, // Target: 10k stitches per week
      isActive: true,
      isAutoGenerated: true,
    },
  });

  return challenge;
}

